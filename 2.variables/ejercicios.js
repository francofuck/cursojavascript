//averiguar la diferencia entre el var y let
//var tiene la función o alcance global.
//let deja tener el alcance del bloque.
//var Se eleva a la parte superior de su alcance y se inicializa sin definir.
//let También se elevó a la parte superior de su alcance, pero no se inicializó.
//var Se puede actualizar o volver a declarar.
//let Solo se puede actualizar y no se puede volver a declarar.
//var Es una forma antigua de declarar una variable.
//let 	Es una nueva forma de declarar variables introducida en ES6.
